    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        jTable1.setDefaultRenderer(Object.class, new jTableSelect());
        jTable1.setRowHeight(30);
        //Se crean las botones
        JButton boton = new JButton("Detalles");
        boton.setName("Detalles");
        
        DefaultTableModel modelo =  new DefaultTableModel(){//Para que no se puedan editar los datos de las tablas
            @Override
            public boolean isCellEditable(int row,int column){
                return false;
        }
        };
        modelo.addColumn("Imagen");
        modelo.addColumn("Estilo de Vehiculo");
        modelo.addColumn("Costo por d√≠a");
        int contador =0;
        this.jTable1.setModel(modelo);
        //Para agregar datos en la fila
        Object O[]=null;
        //Numero de fila
        
        if(entradaPrecioFiltro.isSelected()==false && (Integer.parseInt(entradaEstiloFiltro.getText()))==0 && (Integer.parseInt(entradaCapacidadFiltro.getText()))==0 ){
                JOptionPane.showMessageDialog(null, "Error al realizar la busqueda, ingrese los filtros deseados", "Error de ingreso",JOptionPane.PLAIN_MESSAGE);}
        else{
            //Filtro solo por la el Tipo de Vehiculo
            if(!"".equals(Integer.parseInt(entradaEstiloFiltro.getText())) && entradaPrecioFiltro.isSelected()==false && entradaCapacidadFiltro.getText().equals("") ){
                PrograBases1.vehiculos.getHead();
                PrograBases1.vehiculos.current = PrograBases1.vehiculos.current.getNextNode();
        
                while(PrograBases1.vehiculos.current != null){
                    //Busca el vehiculo
                    if((Integer.parseInt(entradaEstiloFiltro.getText()))==(PrograBases1.vehiculos.current.getElement().idEstilo )&&
                            interfazReserva3.copiaSedeRecogida == (PrograBases1.vehiculos.current.getElement().idSede)){
                        File imagen = PrograBases1.vehiculos.current.getElement().getFoto();
                        int estilo = PrograBases1.vehiculos.current.getElement().getIdEstilo();
                        int costo = PrograBases1.vehiculos.current.getElement().getCostoDia();
                        
                        modelo.addRow(O);
                        modelo.setValueAt(imagen, contador, 0);
                        modelo.setValueAt(estilo, contador, 1);
                        modelo.setValueAt(costo, contador, 2);
                        modelo.setValueAt(boton, contador, 3);

                        contador =  contador + 1;
                        
                    }PrograBases1.vehiculos.current = PrograBases1.vehiculos.current.getNextNode();
                }
            }
            //Filtro de solo la Capacidad del Vehiculo
            else if(!"".equals(Integer.parseInt(entradaCapacidadFiltro.getText())) && entradaPrecioFiltro.isSelected()==false && entradaEstiloFiltro.getText().equals("") ){
                PrograBases1.vehiculos.getHead();
                PrograBases1.vehiculos.current = PrograBases1.vehiculos.current.getNextNode();
        
                while(PrograBases1.vehiculos.current != null){
                    //Busca el vehiculo
                    if((Integer.parseInt(entradaCapacidadFiltro.getText()))==(PrograBases1.vehiculos.current.getElement().getCantidadPersonas() )&&
                            interfazReserva3.copiaSedeRecogida == (PrograBases1.vehiculos.current.getElement().idSede)){
                        File imagen = PrograBases1.vehiculos.current.getElement().getFoto();
                        int estilo = PrograBases1.vehiculos.current.getElement().getIdEstilo();
                        int costo = PrograBases1.vehiculos.current.getElement().getCostoDia();
                        
                        modelo.addRow(O);
                        modelo.setValueAt(imagen, contador, 0);
                        modelo.setValueAt(estilo, contador, 1);
                        modelo.setValueAt(costo, contador, 2);
                        modelo.setValueAt(boton, contador, 3);

                        contador =  contador + 1;
                        
                    }PrograBases1.vehiculos.current = PrograBases1.vehiculos.current.getNextNode();
                }
            }
            //Filtro solo del precio
        }
        
        
        
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

